/**
 * Class representing the keyboard input state.
 * Each property corresponds to a key, which can be pressed or not.
 */
class Keyboard {
  /**
   * Indicates whether the DOWN key (arrow down) is pressed.
   * @type {boolean}
   */
  DOWN = false;

  /**
   * Indicates whether the LEFT key (arrow left) is pressed.
   * @type {boolean}
   */
  LEFT = false;

  /**
   * Indicates whether the RIGHT key (arrow right) is pressed.
   * @type {boolean}
   */
  RIGHT = false;

  /**
   * Indicates whether the SPACE key is pressed (typically used for jumping).
   * @type {boolean}
   */
  SPACE = false;

  /**
   * Indicates whether the UP key (arrow up) is pressed.
   * @type {boolean}
   */
  UP = false;

  /**
   * Indicates whether the F key is pressed (typically used for throwing or action).
   * @type {boolean}
   */
  F = false;
}
